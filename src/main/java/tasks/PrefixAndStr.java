package tasks;

public class PrefixAndStr {

    private final int pref;
    private final String str;

    public PrefixAndStr(int pref, String str) {
        this.pref = pref;
        this.str = str;
    }

    public int getPref() {
        return pref;
    }

    public String getStr() {
        return str;
    }
}
